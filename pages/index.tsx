import * as i from 'types';
import * as React from 'react';
import Head from 'next/head';
import useSWR from 'swr';

import { fetcher } from 'services';
import { useQueryParams } from 'hooks';
import { ProductsFilter, ProductsOverview } from 'modules/products';
import { Container, Pagination } from 'common/layout';
import { Button } from 'common/interaction';

const limit = 21;

const Home: React.FC<HomeProps> = ({
  getProducts, getCategories, getTotalProducts,
}) => {
  const { queryParams } = useQueryParams();
  const [currentPage, setCurrentPage] = React.useState(1);
  const [pages, setPages] = React.useState(Math.floor((getTotalProducts || 0) / limit));

  const variables = React.useMemo(() => ({
    offset: currentPage === 1 ? 0 : (currentPage + 1) * limit,
    limit,
    categories: queryParams?.categories,
  }), [currentPage, queryParams?.categories]);

  const { data, isValidating } = useSWR<{
    getProducts: i.Product[];
    getTotalProducts: number;
  }>(
    [
      `query GetProducts($offset: Int!, $limit: Int!, $categories: [String]) {
        getProducts(offset: $offset, limit: $limit, categories: $categories) {
          name
          image
          categories
        }
        getTotalProducts(categories: $categories)
      }`,
      variables,
    ],
    fetcher,
    {
      initialData: {
        getProducts,
        getTotalProducts,
      },
    },
  );

  React.useEffect(() => {
    const count = Math.floor((data?.getTotalProducts || 0) / limit);
    setPages(count > 0 ? count - 1 : count);
  }, [data?.getTotalProducts]);

  React.useEffect(() => {
    setCurrentPage(1);
  }, [queryParams?.categories]);

  return (
    <Container>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <ProductsFilter categories={getCategories} />

      {data?.getProducts && (
        <ProductsOverview products={data.getProducts} loading={isValidating} />
      )}

      <Pagination>
        {Array.from(new Array(pages).keys()).map((pageNumber) => {
          const current = pageNumber + 1;
          return (
            <Button
              key={`button_${pageNumber}`}
              onClick={() => setCurrentPage(current)}
              active={current === currentPage}
            >
              {current}
            </Button>
          );
        })}
      </Pagination>
    </Container>
  );
};

type HomeProps = {
  getProducts: i.Product[];
  getCategories: string[];
  getTotalProducts: number;
};

export const getServerSideProps = async ({ query }) => {
  const data = await fetcher(
    `query GetProducts($offset: Int!, $limit: Int!, $categories: [String]) {
      getProducts(offset: $offset, limit: $limit, categories: $categories) {
        name
        image
        categories
      }
      getTotalProducts(categories: $categories)
      getCategories
    }`,
    {
      offset: 0,
      limit,
      categories: query?.categories ? query.categories.split(',') : null,
    },
  );

  if (!data) {
    return {
      notFound: true,
    };
  }

  return {
    props: {
      getProducts: data.getProducts,
      getCategories: data.getCategories,
      getTotalProducts: data.getTotalProducts,
    },
  };
};

export default Home;
